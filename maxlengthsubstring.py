# Sample list of strings
strings = ["Hello, world!", "This is a longer string", "Short one", "Maximum length string here"]

# Initialize a variable to store the maximum length
max_length = 0

# Iterate through the strings in the list
for s in strings:
    # Check the length of the current string
    current_length = len(s)

    # Update max_length if the current string is longer
    if current_length > max_length:
        max_length = current_length

# Print the maximum length
print("Maximum length of strings:", max_length)

